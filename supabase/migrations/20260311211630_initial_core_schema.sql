create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.profiles (id, email)
  values (new.id, new.email)
  on conflict (id) do update
    set email = excluded.email,
        updated_at = timezone('utc', now());

  return new;
end;
$$;

create trigger on_auth_user_created
after insert on auth.users
for each row
execute function public.handle_new_user();

create or replace function public.is_organization_member(p_organization_id uuid)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.memberships memberships
    where memberships.organization_id = p_organization_id
      and memberships.user_id = auth.uid()
      and memberships.status = 'active'
  );
$$;

create or replace function public.has_organization_role(p_organization_id uuid, p_roles text[])
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.memberships memberships
    where memberships.organization_id = p_organization_id
      and memberships.user_id = auth.uid()
      and memberships.status = 'active'
      and memberships.role = any (p_roles)
  );
$$;

create or replace function public.create_organization(p_name text, p_slug text default null)
returns public.organizations
language plpgsql
security definer
set search_path = public
as $$
declare
  v_organization public.organizations;
  v_slug text;
begin
  if auth.uid() is null then
    raise exception 'Authentication required';
  end if;

  if p_name is null or btrim(p_name) = '' then
    raise exception 'Organization name is required';
  end if;

  v_slug := coalesce(nullif(lower(btrim(p_slug)), ''), lower(regexp_replace(btrim(p_name), '[^a-zA-Z0-9]+', '-', 'g')));
  v_slug := trim(both '-' from v_slug);

  if v_slug = '' then
    raise exception 'Organization slug is required';
  end if;

  insert into public.organizations (name, slug, owner_user_id)
  values (btrim(p_name), v_slug, auth.uid())
  returning * into v_organization;

  insert into public.memberships (organization_id, user_id, role, status)
  values (v_organization.id, auth.uid(), 'owner', 'active');

  insert into public.activity_logs (organization_id, actor_user_id, action, entity_type, entity_id)
  values (v_organization.id, auth.uid(), 'organization.created', 'organization', v_organization.id);

  return v_organization;
end;
$$;

revoke all on function public.create_organization(text, text) from public;
grant execute on function public.create_organization(text, text) to authenticated;

